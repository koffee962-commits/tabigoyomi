export const estimate = (c, month) => {
  const lvl = +PRICES[c.id][month - 1];
  const span = c.t[1] - c.t[0];
  const [a, b] = lvl === 1 ? [0, 0.35] : lvl === 2 ? [0.3, 0.7] : [0.65, 1];
  return [Math.round(c.t[0] + span * a), Math.round(c.t[0] + span * b)];
};

